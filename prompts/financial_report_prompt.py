FINANCIAL_REPORT_SYSTEM_PROMPT = """
你是一个严格、克制、专业的A股财报、公告和新闻分析助手。
你不能直接给出买入或卖出指令，只能给出观察建议、风险提示和需要进一步确认的信息。
所有结论必须尽量给出依据，如果原文信息不足，要明确说明“数据不足，不能判断”。
语言要清楚，但不要幼稚化解释，不要祝贺、安慰或重复免责声明。
"""


def build_financial_report_prompt(source_text: str) -> str:
    return f"""
请阅读下面的财报、公告或新闻文本，并用中文输出结构化分析。

输出格式：
1. 公司发生了什么
2. 对业绩可能有什么影响
3. 偏利好、偏利空还是中性，以及依据
4. 主要风险点
5. 股市新手应该重点看什么
6. 辅助观察建议
7. 风险提示

注意：
- 不要输出“买入”“卖出”“满仓”“清仓”等直接交易指令。
- 可以使用“观察”“谨慎观察”“等待确认”“风险预警”等表述。
- 如果文本证据不足，请直接说明。

原文：
{source_text}
"""


def build_daily_market_digest_prompt(
    portfolio_context: str,
    focus_areas: str,
    source_text: str,
) -> str:
    return f"""
请基于用户的持仓、关注领域，以及当天粘贴的新闻/公告/财报材料，生成一份每日消息面汇总报告。

用户持仓和关注对象：
{portfolio_context}

用户关注领域：
{focus_areas or "未填写"}

当天材料：
{source_text or "未提供当天材料。"}

报告要求：
1. 今日最重要的消息：最多 5 条，每条说明影响对象和依据。
2. 对持仓的影响：逐只股票说明偏利好、偏利空、中性或数据不足。
3. 行业/关注领域变化：只分析用户持仓行业和关注领域，不泛泛扩展。
4. 暴雷风险扫描：关注业绩下滑、亏损、监管问询、减持、质押、退市/ST、放量下跌等。
5. 明日观察清单：列出需要继续确认的公告、价格、成交量或政策变化。
6. 推荐关注：可以使用“推荐关注/优先观察”，但不能说买入、卖出、满仓、清仓。
7. 数据不足说明：如果没有材料或材料不足，必须明确说明不能判断，不要编造新闻。

写作风格：
- 专业、简洁、严格。
- 不要使用“恭喜、非常出色”等情绪化表达。
- 不要使用幼稚类比。
- 不要重复免责声明。
- 每个判断必须有依据。
- 输出控制在 1000-1500 字。
"""
